def height_men_bmr(height):
    """
    This function calculates the height variable
    which is going to be used in calculating
    the male user's bmr
    """
    # print("This is your height variable used in calculating your bmr\n ")
    heights_bmr_men = height * 4.799
    return heights_bmr_men


def height_women_bmr(height):
    """
    This function calculates the height variable
    which is going to be used in calculating
    the female user's bmr
    """
    # print("This is your height variable used in calculating your bmr\n ")
    heights_bmr_women = height * 3.098
    return heights_bmr_women
